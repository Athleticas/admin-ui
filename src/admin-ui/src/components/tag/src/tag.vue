<template>
  <div class="au-tag au-theme-border-radius--normal" :class="{
    [`au-theme-font-color--${localType}-3`]: !active,
    [`au-theme-border-color--${localType}-3`]: !active,
    [`au-theme-font-color--base-12`]: active,
    [`au-theme-background-color--${localType}-3`]: active,
    [`au-size-${size}-bordered`]: !active,
    [`au-size-${size}`]: active
  }" :style="{
    border: active ? 'none' : ''
  }">
    <slot></slot>
    <span class="au-tag-close" v-show="closable" :class="{
      [`au-theme-hover-background-color--${localType}-3`]: !active,
      [`au-theme-hover-background-color--base-12`]: active,
      [`au-theme-hover-font-color--base-12`]: !active,
      [`au-theme-hover-font-color--${localType}-3`]: active
    }" @click="$emit('close')"><au-icon class="au-tag-close-icon" type="times"/></span>
  </div>
</template>
<script>
  import auIcon from '../../icon'
  export default {
    name: 'au-tag',
    components: { auIcon },
    props: {
      type: {
        type: String,
        default: 'primary'
      },
      closable: Boolean,
      size: {
        type: String,
        default: 'normal'
      },
      active: Boolean
    },
    data () {
      return {}
    },
    computed: {
      localType () {
        if (this.type === 'default') return 'base'
        else return this.type
      }
    }
  }
</script>
