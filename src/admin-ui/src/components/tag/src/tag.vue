  @import '../../../style/_vars.scss';
  @import '../../../style/_size.scss';
  .au-tag {
    display: inline-block;
    padding: 0 10px;
    border-width: 1px;
    border-style: solid;
    font-size: $normal;
  }
  .au-tag-close {
    display: inline-block;
    position: relative;
    top: -1px;
    width: 18px;
    height: 18px;
    line-height: 18px;
    border-radius: 100%;
    margin-left: 5px;
    text-align: center;
    cursor: pointer;
    vertical-align: middle;
  }
